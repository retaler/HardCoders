package com.gmail.grezin.v;

public class Flower {
	private String flowerName;
	private String petalsColor;

	public Flower(String flowerName, String petalsColor) {
		super();
		this.flowerName = flowerName;
		this.petalsColor = petalsColor;
	}

	public String getFlowerName() {
		return flowerName;
	}

	public void setFlowerName(String flowerName) {
		this.flowerName = flowerName;
	}

	public String getPetalsColor() {
		return petalsColor;
	}

	public void setPetalsColor(String petalsColor) {
		this.petalsColor = petalsColor;
	}

	
}
